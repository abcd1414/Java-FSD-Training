public class  NestedIfElse
{
public static void main(String[] args)
{
if(20<50)
{
	if(2<1)
		System.out.println("20 is less than 50 & 2 is also less than 1");
	else
		System.out.println("20 is less than 50 but 2 is not less than 1");
}
else 
	System.out.println("20 is not less than 50");
}
}